
public class Pair {
    private Cab x ;
    private boolean y ;

    public Pair(Cab x, boolean y)
    {
        this.x = x;
        this.y = y;
    }


    public Cab getX() {
        return x;
    }

    public void setX(Cab x) {
        this.x = x;
    }

    public boolean getY() {
        return y;
    }

    public void setY(boolean y) {
        this.y = y;
    }

}